#include <stdio.h>
// switch

int main(){
    int Number = 23 ;

    switch(Number){
        case 0:
        printf("Number Is 0 ");
        break;
        case 1:
        printf("Number Is 1 ");
        break;
        case 2:
        printf("Number Is 2 ");
        break;
        default:
        printf("Invalid");
        break;
    }
}



